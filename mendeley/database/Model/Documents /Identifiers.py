class Identifiers:
    """
    Document Identifiers
    """
    def __init__(self, issn, isb, pmid, arxiv, doi, scopus, doc_id):
        """
        Constructor
        @param issn:
        @param isb:
        @param pmid:
        @param arxiv:
        @param doi:
        @param scopus:
        @param doc_id:
        """
        self.issn = issn
        self.isb = isb
        self.pmid = pmid
        self.arxiv = arxiv
        self.doi = doi
        self.scopus = scopus
        self.doc_id = doc_id